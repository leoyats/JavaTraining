public class javaRushtriangle {
    public static void main(String[] args) throws Exception {
        int a = 8;
        for (int i = 0; i < 10; i++) {
            System.out.print(a);
            for (int j = 0; j < i; j++) {
                System.out.print(a);
            }
            System.out.println();
        }

    }
}
