public class P_003_VariablesOperation {
    public static void main(String[] args) {
        int iA = 5;
        int iB = 4;
        System.out.println(iA + iB);

        char cA = '5';
        char cB = '4';
        System.out.println(cA + cB);

        String sA = "5";
        String sB = "4";
        System.out.println(sA + sB);
    }
}
